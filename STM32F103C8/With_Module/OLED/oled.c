// ========== OLED I2C BASIC ==========
void I2C1_Start(uint8_t address, uint8_t direction) {
    while(I2C_GetFlagStatus(I2C1,I2C_FLAG_BUSY));
    I2C_GenerateSTART(I2C1, ENABLE);
    while(!I2C_CheckEvent(I2C1,I2C_EVENT_MASTER_MODE_SELECT));
    I2C_Send7bitAddress(I2C1, address, direction);
    if(direction == I2C_Direction_Transmitter)
        while(!I2C_CheckEvent(I2C1,I2C_EVENT_MASTER_TRANSMITTER_MODE_SELECTED));
    else
        while(!I2C_CheckEvent(I2C1,I2C_EVENT_MASTER_RECEIVER_MODE_SELECTED));
}

void I2C1_Write(uint8_t data){
    I2C_SendData(I2C1,data);
    while(!I2C_CheckEvent(I2C1,I2C_EVENT_MASTER_BYTE_TRANSMITTED));
}

void I2C1_Stop(void){
    I2C_GenerateSTOP(I2C1, ENABLE);
}

// ========== OLED LOW-LEVEL ==========
void OLED_WriteCommand(uint8_t cmd){
    I2C1_Start(OLED_I2C_ADDR,I2C_Direction_Transmitter);
    I2C1_Write(0x00); // control byte = command
    I2C1_Write(cmd);
    I2C1_Stop();
}

void OLED_WriteData(uint8_t data){
    I2C1_Start(OLED_I2C_ADDR,I2C_Direction_Transmitter);
    I2C1_Write(0x40); // control byte = data
    I2C1_Write(data);
    I2C1_Stop();
}

// ========== OLED BUFFER ==========
void OLED_Clear(void){
    memset(OLED_Buffer,0,sizeof(OLED_Buffer));
}

void OLED_UpdateScreen(void){
    for(uint8_t page=0; page< OLED_HEIGHT/8; page++){
        OLED_WriteCommand(0xB0 + page);
        OLED_WriteCommand(0x00);
        OLED_WriteCommand(0x10);
        for(uint8_t x=0; x<OLED_WIDTH; x++){
            OLED_WriteData(OLED_Buffer[OLED_WIDTH*page + x]);
        }
    }
}

void OLED_DrawPixel(uint8_t x, uint8_t y, bool color){
    if(x>=OLED_WIDTH || y>=OLED_HEIGHT) return;
    if(color)
        OLED_Buffer[x + (y/8)*OLED_WIDTH] |= (1 << (y%8));
    else
        OLED_Buffer[x + (y/8)*OLED_WIDTH] &= ~(1 << (y%8));
}

// ========== TEXT ==========
void OLED_GotoXY(uint8_t x, uint8_t y){
    CurrentX = x;
    CurrentY = y;
}

void OLED_Puts(char* str){
    while(*str){
        char c = *str++;
        if(c<32 || c>126) c='?';
        for(int i=0;i<5;i++){
            uint8_t line = Font5x7[c-32][i];
            for(int j=0;j<8;j++){
                OLED_DrawPixel(CurrentX + i, CurrentY + j, (line >> j)&1);
            }
        }
        CurrentX += 6; // spacing
    }
}

// ========== BITMAP ==========
void OLED_DrawBitmap(uint8_t x,uint8_t y,const uint8_t* bitmap,uint8_t w,uint8_t h){
    uint16_t bytePerRow = (w+7)/8;
    for(uint8_t j=0;j<h;j++){
        for(uint8_t i=0;i<w;i++){
            uint16_t byteIndex = j*bytePerRow + (i/8);
            uint8_t bitMask = 0x80 >> (i%8);
            if(bitmap[byteIndex] & bitMask)
                OLED_DrawPixel(x+i, y+j, 1);
            else
                OLED_DrawPixel(x+i, y+j, 0);
        }
    }
}

// ========== INIT ==========
void OLED_Init(void){
    // c?u hěnh I2C1
    GPIO_InitTypeDef gpio;
    I2C_InitTypeDef i2c;
    RCC_APB2PeriphClockCmd(RCC_APB2Periph_GPIOB, ENABLE);
    RCC_APB1PeriphClockCmd(RCC_APB1Periph_I2C1, ENABLE);

    gpio.GPIO_Pin = GPIO_Pin_6 | GPIO_Pin_7;
    gpio.GPIO_Mode = GPIO_Mode_AF_OD;
    gpio.GPIO_Speed = GPIO_Speed_50MHz;
    GPIO_Init(GPIOB,&gpio);

    I2C_DeInit(I2C1);
    i2c.I2C_Mode = I2C_Mode_I2C;
    i2c.I2C_ClockSpeed = 400000;
    i2c.I2C_DutyCycle = I2C_DutyCycle_2;
    i2c.I2C_OwnAddress1 = 0;
    i2c.I2C_Ack = I2C_Ack_Enable;
    i2c.I2C_AcknowledgedAddress = I2C_AcknowledgedAddress_7bit;
    I2C_Init(I2C1,&i2c);
    I2C_Cmd(I2C1,ENABLE);

    // Init sequence
    OLED_WriteCommand(0xAE);
    OLED_WriteCommand(0x20); OLED_WriteCommand(0x00);
    OLED_WriteCommand(0xB0);
    OLED_WriteCommand(0xC8);
    OLED_WriteCommand(0x00);
    OLED_WriteCommand(0x10);
    OLED_WriteCommand(0x40);
    OLED_WriteCommand(0x81); OLED_WriteCommand(0x7F);
    OLED_WriteCommand(0xA1);
    OLED_WriteCommand(0xA6);
    OLED_WriteCommand(0xA8); OLED_WriteCommand(OLED_HEIGHT-1);
    OLED_WriteCommand(0xA4);
    OLED_WriteCommand(0xD3); OLED_WriteCommand(0x00);
    OLED_WriteCommand(0xD5); OLED_WriteCommand(0x80);
    OLED_WriteCommand(0xD9); OLED_WriteCommand(0xF1);
    OLED_WriteCommand(0xDA); OLED_WriteCommand(0x12);
    OLED_WriteCommand(0xDB); OLED_WriteCommand(0x40);
    OLED_WriteCommand(0x8D); OLED_WriteCommand(0x14);
    OLED_WriteCommand(0xAF);

    OLED_Clear();
    OLED_UpdateScreen();
}

